static struct EmbedImage {
    int width, height, depth;
    const unsigned char *data;
    int numColors;
    const QRgb *colorTable;
    bool alpha;
    const char *name;
} embed_image_vec[] = {
    { 22, 22, 32, (const unsigned char*)fileopen_data, 0, 0, TRUE, "fileopen" },
    { 22, 22, 32, (const unsigned char*)filesave_data, 0, 0, TRUE, "filesave" },
    { 22, 22, 32, (const unsigned char*)print_data, 0, 0, TRUE, "print" },
    { 0, 0, 0, 0, 0, 0, 0, 0 }
};

static QImage& uic_findImage_verde_gui( const char *name )
{
    static QDict<QImage> dict;
    QImage* img = dict.find(name);
    if ( !img ) {
        for (int i=0; embed_image_vec[i].data; i++) {
	if ( 0==strcmp(embed_image_vec[i].name, name) ) {
	    img = new QImage((uchar*)embed_image_vec[i].data,
			embed_image_vec[i].width,
			embed_image_vec[i].height,
			embed_image_vec[i].depth,
			(QRgb*)embed_image_vec[i].colorTable,
			embed_image_vec[i].numColors,
			QImage::BigEndian
		);
	    if ( embed_image_vec[i].alpha )
	        img->setAlphaBuffer(TRUE);
	    break;
	}
        }
        if ( !img ) {
            static QImage dummy;
            return dummy;
        }
    }
    return *img;
}

class DesignerMimeSourceFactory : public QMimeSourceFactory
{
public:
    DesignerMimeSourceFactory() {}
    const QMimeSource* data( const QString& abs_name ) const {
	QImage img = uic_findImage_verde_gui( abs_name );
	if ( !img.isNull() ) {
	    QPixmap pix;
	    pix.convertFromImage( img );
	    QMimeSourceFactory::defaultFactory()->setPixmap( abs_name, pix );
	    return QMimeSourceFactory::defaultFactory()->data( abs_name );
	} else {
	    QMimeSourceFactory::removeFactory( (QMimeSourceFactory*)this );
	    const QMimeSource *s = QMimeSourceFactory::defaultFactory()->data( abs_name );
	    QMimeSourceFactory::addFactory( (QMimeSourceFactory*)this );
	    return s;
	}
	return 0;
    };
};
static DesignerMimeSourceFactory *designerMimeSourceFactory = 0;
static void qInitImages()
{
    if ( designerMimeSourceFactory )
	return;
    designerMimeSourceFactory = new DesignerMimeSourceFactory;
    QMimeSourceFactory::defaultFactory()->addFactory( designerMimeSourceFactory );
}
static void qCleanupImages()
{
    if ( !designerMimeSourceFactory )
	return;
    QMimeSourceFactory::defaultFactory()->removeFactory( designerMimeSourceFactory );
    delete designerMimeSourceFactory;
    designerMimeSourceFactory = 0;
}
class StaticInitImages
{
public:
    StaticInitImages() { qInitImages(); }
    ~StaticInitImages() { qCleanupImages(); }
};
